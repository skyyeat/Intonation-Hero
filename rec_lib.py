def dialog_get_recording(ex, n):
	ec = Exercises[ex]
	call = (n-1)*2
	rec = ec[call]
	response = ec[call+1]
	nlst = []
	nlst.append(rec)
	nlst.append(response)
	return nlst

def repeat_get_recording(ex, n):
	ec = Exercises[ex]
	rec = ec[n-1]
	response = ec[n-1]
	nlst = []
	nlst.append(rec)
	nlst.append(response) 
	return nlst


